## Strings ## Cadena de caracteres

#Distintas formas de escribir un string
print("Hello world")
print('Hello world')
print('''Hello world''')
print("""Hello world""")
#Identificador del tipo de dato (en este caso, str)
print(type("Hello world"))
#Concatenación de strings
print("Hello" + "world")

## Integers ## Valores numéricos enteros.
0, 1, 2, ...

## Float ## Valores numéricos exactos.
0., 1.3, 2.36705, ...

## Boolean ## Valores booleanos: true or false
True, False

## List ## Conjunto de datos de igual o distinto tipo
[]
[10, 20, 30, 40]
["Pepe", "Juan", "Ana", "María"]
[10, True, "Pepe", 10.2]

## Tuples ## Listas inmutables

()
(10, 20, 30, 40)

