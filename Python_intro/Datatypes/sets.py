# Colección no indexada de elementos
colors = {"Red", "Green", "Blue"}

# __ in __ -> Comprueba si existe un elemento en un set
"hello" in colors
"Red" in colors

# del__ -> Elimina el set
colors.add("Violet")

# .add(element) -> Método que añade un elemento al inicio del set
colors.add("Violet")

# .remove(element) -> Método que elimina un elemento al inicio del set
colors.add("Violet")

# .clear() -> Método que limpia el set
colors.add("Violet")

